package kr.co.sist.expression;

public class Expression01 {

	public static void main(String[] args) {
		//컴퓨터는 다양한 일을 계산 해서 처리한다.
		//식(expression)을 배운다.
		//1+2
		//연산자(+):operator
		//피연산자(1):operand
		
		System.out.println("1+2는 " + (1+2) + "입니다.");//식이 평가되어:3출력
        
		System.out.println("3*4는 " + (3*4) + "입니다.");//

	}

}
