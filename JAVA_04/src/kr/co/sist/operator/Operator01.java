package kr.co.sist.operator;

public class Operator01 {

	public static void main(String[] args) {
        
		int num1 = 10;
		int num2 = 5;
		
		System.out.println("num1과 num2로 다양한 연산을 수행합니다.");
        
		System.out.println("num1+num2는 " + (num1+num2) + "입니다.");
        
		System.out.println("num1-num2는 " + (num1-num2) + "입니다.");
        
		System.out.println("num1*num2는 " + (num1*num2) + "입니다.");
        
		System.out.println("num1/num2는 " + (num1/num2) + "입니다.");//몪
        
		System.out.println("num1%num2는 " + (num1%num2) + "입니다.");//나머지

	}
//	num1과 num2로 다양한 연산을 수행합니다.
//	num1+num2는 15입니다.
//	num1-num2는 5입니다.
//	num1*num2는 50입니다.
//	num1/num2는 2입니다.
//	num1%num2는 0입니다.
}
